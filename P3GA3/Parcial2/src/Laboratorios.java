    public void MostrarDB(String Tabla) {
        String[] columnas = new String[4];
        String query;
        try {

            Connection c = DriverManager.getConnection(Principal.BD, Principal.Usuario, Principal.Contrase침a);
            query = "SELECT * FROM " + Tabla;

            PreparedStatement consulta = c.prepareStatement(query);
            ResultSet resultado = consulta.executeQuery();

            DefaultTableModel md = new DefaultTableModel(null, NombresColumnasLab);
            tblLab.setModel(md);

            while (resultado.next()) {
                
                for (int i = 0; i < 4; i++) {
                    columnas[i] = resultado.getString(i+2);
                }

            }

            md.addRow(columnas);
        }

    
    catch (Exception err) {
            err.printStackTrace();
    }

}
    

